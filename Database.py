        "            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,\n",
        "            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,\n",
        "            FOREIGN KEY (user_id) REFERENCES users(id)\n",
        "        )''')\n",
