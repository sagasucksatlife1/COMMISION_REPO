        "\n",
        "    def init_db(self):\n",
        "        conn = self.get_connection()\n",
        "        cursor = conn.cursor()\n",
        "        cursor.execute('''CREATE TABLE IF NOT EXISTS users (\n",
        "            id INTEGER PRIMARY KEY AUTOINCREMENT,\n",
        "            username TEXT UNIQUE NOT NULL,\n",
        "            password_hash TEXT NOT NULL,\n",
        "            role TEXT NOT NULL CHECK(role IN ('employee', 'admin')),\n",
        "            full_name TEXT NOT NULL,\n",
        "            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP\n",
        "        )''')\n",
        "        cursor.execute('''CREATE TABLE IF NOT EXISTS commissions (\n",
        "            id INTEGER PRIMARY KEY AUTOINCREMENT,\n",
        "            user_id INTEGER NOT NULL,\n",
        "            sale_date DATE NOT NULL,\n",
        "            unlisted_sales REAL NOT NULL DEFAULT 0,\n",
        "            loans REAL NOT NULL DEFAULT 0,\n",
        "            third_party_sales REAL NOT NULL DEFAULT 0,\n",
        "            calculated_commission REAL NOT NULL,\n",
